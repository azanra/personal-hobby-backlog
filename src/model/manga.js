export default function Manga(
  image,
  title,
  alias,
  currentChapter,
  status,
  published,
  genre,
  demographic,
  serialization,
  author
) {
  this.image = image;
  this.title = title;
  this.alias = alias;
  this.currentChapter = currentChapter;
  this.status = status;
  this.published = published;
  this.genre = genre;
  this.demographic = demographic;
  this.serialization = serialization;
  this.author = author;
}
