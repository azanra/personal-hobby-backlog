export default function Anime(
  image,
  title,
  alias,
  type,
  episode,
  aired,
  producer,
  studios,
  source,
  genre,
  duration,
  rating,
  synopsis
) {
  this.image = image;
  this.title = title;
  this.alias = alias;
  this.type = type;
  this.episode = episode;
  this.aired = aired;
  this.producer = producer;
  this.studios = studios;
  this.source = source;
  this.genre = genre;
  this.duration = duration;
  this.rating = rating;
  this.synopsis = synopsis;
}
